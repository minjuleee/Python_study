from datetime import datetime, date, time, timedelta

now = datetime.now()  # 현재날짜 시간
print(now)
print(now.year)    # 년
print(now.month)   # 월
print(now.day)     # 일
print(now.hour)    # 시
print(now.minute)  # 분
print(now.second)  # 초

d = date(2025, 12, 30)  # date() 생성자를 통해 날짜 객체 생성
print(d)

