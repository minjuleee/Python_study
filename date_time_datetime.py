print(t)

dt = datetime(2025, 12, 30, 9, 0, 0)
print(dt)

dt = datetime.strptime("2025-03-15 10:03:00", "%Y-%m-%d %H:%M:%S")  # 문자열을 -> 날짜 시간 객체로 변환
print(dt)

print(dt.strftime("%Y/%m/%d 시간 %H:%M:%S"))  # 날짜시간객체들 -> 문자열로 변환

today = date.today()
print(today + timedelta(days=7))
print(today - timedelta(days=30))

