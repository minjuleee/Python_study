# 파이썬 연산자 종류

# 1. 산술 연산자
# 

a = 4; b = 3
print(a+b, a-b, a*b, a/b, a//b, a*b, a**b)

# 문자열끼리의 덧셈은 문자열이 연결된다.
name = '홍길동'
job = '은 학생입니다'

print(name + job)

# 산술연산자에서 ()를 이용하여 우선순위를 정할 수 있다.
print((3-2) * 4 + 5 / 12)

# 2. 대입 연산자
x = 3
x += 3    # x = x + 3
print(x)

x *= 2  # x = x * 2
print(x)

x -= 5  # x = x - 5
print(x)

x /= 7  # x = x / 7
print(x)



